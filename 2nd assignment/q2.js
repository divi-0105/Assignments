var grade = prompt("Enter a Grade :");
switch (grade) {
    case 'A':
        alert("Excellent");
        break;
    case 'B':
        alert("Good");
        break;
    case 'C':
        alert("Fair");
        break;
    case 'D':
        alert("Poor");
        break;
    default:
        alert("Wrong Grade.........");
}
